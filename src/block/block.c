UINT8 current_rotation = 0;
UINT8 shape_type = 0;
UINT8 tiles[7] = {2, 3, 4, 5, 6, 7, 8};

void rotate(){
    current_rotation++;
    if(current_rotation > 3)
        current_rotation = 0;
}

void new_block(UINT8 type){
    shape_type = type;
    current_rotation = 0;
}

BOOLEAN** get_grid(){
     return shape_grids[shape_type][current_rotation];
}

UINT8 get_tile(){
    return tiles[shape_type];
}


